# This program takes two integers inputs from the user. The program will return
# the following:
# sum, difference, product, quotient, remainder, exponent

# Inputs that will be used in operations
first_number_input = int(input("Enter the first number: "))
second_number_input = int(input("Enter the second number: "))

inputs_sum = first_number_input + second_number_input

print(inputs_sum) # The sum of the two integers

inputs_difference = first_number_input - second_number_input

print(inputs_difference) # The difference of the two integers

inputs_product = first_number_input * second_number_input

print(inputs_product) # The product of the two integers

inputs_quotient = first_number_input / second_number_input

print(inputs_quotient) # The quotient of the two integers

inputs_remainder = first_number_input % second_number_input

print(inputs_remainder) # The remainder of the two integers

inputs_exponent = first_number_input ** second_number_input

print(inputs_exponent) # The exponential value of the two integers